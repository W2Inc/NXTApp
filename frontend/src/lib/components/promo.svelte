		<h2>Problems in the current formal education model</h2>
		<table>
			<thead>
				<tr>
					<th>Problem</th>
					<th>Solution</th>
				</tr>
			</thead>
			<tbody>
				<tr>
					<th>Lack of Personalization, Lack of Choice</th>
					<td>
						<details>
							<summary>Students define their own Learning Path</summary>
							<hr />
							<p>
								Our learning platform goes beyond the traditional idea of subjects like
								Math, Physics, Languages or History. Because the content will be created
								by the community, the possibilities are endless.
							</p>

							<p>Some possibilities are:</p>
							<ul>
								<li>Cooking</li>
								<li>Gardening</li>
								<li>Woodworking</li>
								<li>Film Making</li>
								<li>Medicine</li>
								<li>Law</li>
								<li>Anime</li>
								<li>Design</li>
								<li>Architecture</li>
								<li>Music</li>
							</ul>

							<p>
								It's also possible a project encompasses elements from a combination of
								subjects. For example, a product design project could be a combination of
								Math, Physics, Engineering and Art.
							</p>
						</details>
					</td>
				</tr>
				<tr>
					<th>Bad Feedback from exams / work </th>
					<td>
						<details>
							<summary>Feedback from peers and teachers is built in to the system</summary
							>
							<hr />
							<p>
								<Link href="https://www.lifescied.org/doi/10.1187/cbe.cbe-14-03-0054">
									Grading isn't feedback.
								</Link>
								In most cases the grade is perceived as more important than the learning.
							</p>

							<blockquote>
								<p>
									This work affirms an observation that many classroom teachers have made
									about their students: if a paper is returned with both a grade and a
									comment, many students will pay attention to the grade and ignore the
									comment.
								</p>
								<Link href="https://www.lifescied.org/doi/10.1187/cbe.cbe-14-03-0054#B8">
									—Brookhart (2008, p. 8)
								</Link>
							</blockquote>

							<p></p>

							<p>
								We want to change this by making feedback the only source of for grading.
								There are no grades, only feedback. This feedback can come from peers,
								teachers, or anyone else who is interested in the project.
							</p>
						</details>
					</td>
				</tr>
				<tr>
					<th>Focus on certificates and diplomas and less on workings.</th>
					<td>
						<details>
							<summary>
								The proof is in the pudding. Students hand in their work and make it
								visible to the public.
							</summary>
							<hr />
							<p>
								While these are seen as a “proof” of learning, they remove the focus from
								what was actually learned. For instance all the effort that was put into
								notes that were done during lectures / classes are often lost upon
								graduation.
							</p>
							<p>
								Instead, we want to certify that a student has learned something by having
								them show their work to the public.
							</p>
							<p>
								CV wise this carries more weight as experts in the field can actually
								engage and see the work you've done. They can interact with the comments
								and feedback given by others and get a overall more in depth view of your
								skills.
							</p>
						</details>
					</td>
				</tr>
				<tr>
					<th>Lack of overview of students.</th>
					<td>
						<details>
							<summary
								>Student progress is tracked and easily evaluated by teachers and other
								school staff
							</summary>
							<p>
								Teachers often resort to many different solutions to keep track of their
								students. A popular approach is to use a LMS (Learning Management System)
								like Moodle or Blackboard.
							</p>

							<p>
								We want to provide a more intuitive and user-friendly way to track and
								engage with students regarding their progress. (Like our example of a personalized learning graph at the top of this page)
							</p>
						</details>
					</td>
				</tr>
				<tr>
					<th>Failure is punished</th>
					<td>
						<details>
							<summary>
								In our system, "failure" is seen as an actual learning opportunity.
							</summary>
							<hr />

							<p>
								While almost everyone agrees that it's okay and even important to fail,
								and many schools will say they encourage failing.
							</p>

							<p>In reality failure in school is punished:</p>
							<ul>
								<li>Getting a bad grade</li>
								<li>Redoing a year</li>
								<li>Mandatory extra classes</li>
								<li>...</li>
							</ul>

							<p>
								We eliminate this by not having grades and by not having a fixed pace. If
								you "fail", you just try again. In fact "failure" is simply determined if
								a student deems that a learning objective has not been met.
							</p>
						</details>
					</td>
				</tr>
			</tbody>
		</table>

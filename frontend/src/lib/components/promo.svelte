		<h1>FAQ</h1>
		<details>
			<summary>What is your goal and how does it support education?</summary>
			<hr />
			<p>
				Our main goal is to create a learning model based on research (like Self
				Determination Theory) so students can be focussed on their own growth instead of
				grades and academic achievements.
			</p>
		</details>
		<details>
			<summary
				>How can teachers integrate your platform into their existing curriculum?</summary
			>
			<hr />
			<p>
				Our platform is/will be accessible for anyone to create projects and share them
				with students. It helps you set up a curriculum while giving a lot of independence
				and options for students to be in charge of their own learning.
			</p>
			<p>
				It also puts a strong focus on learning goals (“concepts”) more than specific
				projects, allowing students to learn the same concepts in multiple different
				projects created by the community.
			</p>
		</details>

		<details>
			<summary>What features do you offer to enhance student learning?</summary>
			<hr />
			<p>
				Our model puts trust in the learner. We want intrinsic motivation to be the core,
				so we don't offer external rewards for this reason. We not only agree with “You're
				learning for yourself, not for anyone else,” but we also think it's time to apply
				it because way too often children realize that grades, diplomas, and social
				pressure are the reasons why they learn.
			</p>

			<p>
				We want to reduce this and work towards a model that fixes it. Students can
				subscribe to any existing project without restrictions, letting them study
				anything that catches their interest. Every user of the platform can create or
				submit updates to projects, helping maintain things up to date and the overall
				quality.
			</p>
		</details>

		<details>
			<summary>Are physical schools obsolete?</summary>
			<hr />
			<p>
				No! The platform is accessible to everyone online, but schools are essential for
				social development and learning to work in a team. However, the pandemic has shown
				that a lot of learning can also be done online.
			</p>
			<p>
				A connected platform allows your top students in art, math, or kernel development
				to find similarly minded students from anywhere to discuss, provide feedback, or
				work together on advanced topics, but it doesn't compete with the physical space
				to meet, share, and learn.
			</p>

			<p>We view it as a more hybrid approach.</p>
		</details>

		<details>
			<summary>What technical requirements are needed to use your platform?</summary>
			<hr />
			<p>
				We are building a web-based platform, so all you need is a modern web browser.
			</p>
			<p>
				Each school is free to provide the environment that best suits their needs. Wether
				it's a computer lab, their own devices, or really anything else.
			</p>
